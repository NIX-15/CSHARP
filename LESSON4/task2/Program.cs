void displayImage(int[,] mat)
{
    for (int x = 0; x < mat.GetLength(0); x++)
    {
        for (int y = 0; y < mat.GetLength(1); y++)
        {
            if (mat[x, y] == 0) Console.Write(" ");
            else Console.Write("8");
        }
        Console.WriteLine();
    }
}
void imageFill(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        imageFill(row - 1, col);
        imageFill(row, col - 1);
        imageFill(row + 1, col);
        imageFill(row, col + 1);
    }

}
